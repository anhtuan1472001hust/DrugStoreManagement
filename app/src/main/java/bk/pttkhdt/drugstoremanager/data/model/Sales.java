package bk.pttkhdt.drugstoremanager.data.model;

public class Sales {

    private long total;

    private String date;

    public Sales() {
    }

    public long getTotal() {
        return total;
    }

    public void setTotal(long total) {
        this.total = total;
    }


    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }
}
